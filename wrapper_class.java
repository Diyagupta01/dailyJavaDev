public class wrapper_class {
   public static void main(String[] args) {
 
    //wrapper class : provides a way to use primitive data types as refernence data types as they contains useful methods that can be used with collections (ex: ArrayList)
    //primitive   //wrapper
    //----------------------
    //int          //Integer
    //double       //Double
    //boolean      //Boolean
    //char         //Character

// autoboxing = the automatic conversion that the Java compiler makes between the primitive type and reference type
//unboxing = the reverse of autoboxing. Automatic conversion of wrapper class to primitive

Boolean a = true;
Character b = '@';
Integer C = 123;
Double d = 3.14;
String e = "Diyaaaa";

System.out.println(a);
System.out.println(b);
System.out.println(C);
System.out.println(d);
System.out.println(e);

    
   }
}
